module imem (
  input  [31:0] addr,
  output [31:0] instr
);

  reg [31:0] mem [0:15];

  initial begin
    mem[0] = 32'b000000001010_00000_000_00001_0010011; // ADDI x1, x0, 10
    mem[1] = 32'b000000010100_00000_000_00010_0010011; // ADDI x2, x0, 20
    mem[2] = 32'b0000000_00010_00001_000_00011_0110011; // ADD x3, x1, x2
    mem[3] = 32'b0000000_00011_00000_010_00000_0100011; // SW x3, 0(x0)
    mem[4] = 32'b000000000000_00000_010_00100_0000011;  // LW x4, 0(x0)
    mem[5] = 32'b0;
  end

  assign instr = mem[addr[5:2]];

endmodule